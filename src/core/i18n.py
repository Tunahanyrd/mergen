class I18n:
    _lang = "en"

    @classmethod
    def set_language(cls, lang_code):
        if lang_code in TRANS:
            cls._lang = lang_code
        else:
            cls._lang = "en"

    @classmethod
    def get(cls, key):
        return TRANS.get(cls._lang, {}).get(key, key)

    @classmethod
    def detect_os_lang(cls):
        try:
            loc = locale.getdefaultlocale()[0]
            if loc and loc.startswith("tr"):
                return "tr"
        except Exception:
            pass
        return "en"


# Auto init
I18n.set_language(I18n.detect_os_lang())
