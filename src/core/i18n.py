    def get(cls, key):
        return TRANS.get(cls._lang, {}).get(key, key)

    @classmethod
    def detect_os_lang(cls):
        try:
            loc, _ = locale.getlocale()
            if not loc:
                loc = os.getenv("LANG", "en_US").split(".")[0]
            if loc and loc.lower().startswith("tr"):
                return "tr"
        except Exception:
            pass
        return "en"


